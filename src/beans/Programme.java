package beans;

public class Programme {
	
	
	private int numNiv;
	private int codem;
	private int vhcours;
	private int vhtd;
	private int vhtp;
	
	
	public int getNumNiv() {
		return numNiv;
	}
	public void setNumNiv(int numNiv) {
		this.numNiv = numNiv;
	}
	public int getCodem() {
		return codem;
	}
	public void setCodem(int codem) {
		this.codem = codem;
	}
	public int getVhcours() {
		return vhcours;
	}
	public void setVhcours(int vhcours) {
		this.vhcours = vhcours;
	}
	public int getVhtd() {
		return vhtd;
	}
	public void setVhtd(int vhtd) {
		this.vhtd = vhtd;
	}
	public int getVhtp() {
		return vhtp;
	}
	public void setVhtp(int vhtp) {
		this.vhtp = vhtp;
	}
}
