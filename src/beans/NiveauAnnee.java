package beans;

public class NiveauAnnee {
	
	private int numNiv;
	private String anneeUniversitaire;
	private int nbpec;
	private int nbpetd;
	private int nbpetp;
	
	
	public int getNumNiv() {
		return numNiv;
	}
	
	public void setNumNiv(int numNiv) {
		this.numNiv = numNiv;
	}
	
	public String getAnneeUniversitaire() {
		return anneeUniversitaire;
	}
	public void setAnneeUniversitaire(String anneeUniversitaire) {
		this.anneeUniversitaire = anneeUniversitaire;
	}
	public int getNbpec() {
		return nbpec;
	}
	public void setNbpec(int nbpec) {
		this.nbpec = nbpec;
	}
	public int getNbpetd() {
		return nbpetd;
	}
	public void setNbpetd(int nbpetd) {
		this.nbpetd = nbpetd;
	}
	public int getNbpetp() {
		return nbpetp;
	}
	public void setNbpetp(int nbpetp) {
		this.nbpetp = nbpetp;
	}

}
